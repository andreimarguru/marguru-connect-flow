export const useTranslation = (language: string = 'en') => {
  const safeLanguage = Object.keys(translations).includes(language) ? language : 'en';
  
  const t = (key: string, params?: Record<string, any>): string => {
    let text = translations[safeLanguage][key] || translations.en[key] || key;
    
    // Replace parameters in the text if provided
    if (params) {
      Object.entries(params).forEach(([key, value]) => {
        text = text.replace(`%{${key}}`, value);
      });
    }
    
    return text;
  };
  
  return { t };
};

